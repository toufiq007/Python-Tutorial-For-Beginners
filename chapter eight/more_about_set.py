
# there is two operator union and intersection in sets


# union means every item of the different sets
# the symbol is pipe --> |

s1 ={1,2,3,4}
s2 = {3,4,5,6}

s = s1 | s2
# print(s)

# intersection means common item of every sets
# the symbol is & --> &

s1 ={1,2,3,4}
s2 = {3,4,5,6}

s = s1 & s2
